package com.techelevator.model;

public class SubListGrocery {

    long ingredientGroceryList;
    long groceryListListId;
    String text;

    public long getIngredientGroceryList() {
        return ingredientGroceryList;
    }

    public void setIngredientGroceryList(long ingredientGroceryList) {
        this.ingredientGroceryList = ingredientGroceryList;
    }

    public long getGroceryListListId() {
        return groceryListListId;
    }

    public void setGroceryListListId(long groceryListListId) {
        this.groceryListListId = groceryListListId;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }
}
